/******************************************************************************
 * 文件名: sys.c
 * 描  述: 系统配置模块
 * 说  明: NVIC中断优先级分组配置
 *       设置NVIC_PriorityGroup_2，即2位抢占优先级(0-3) + 2位响应优先级(0-3)
 *       抢占优先级高的中断可打断抢占优先级低的中断
 *       相同抢占优先级的中断按响应优先级决定执行顺序
 ******************************************************************************/
#include "sys.h"

/******************************************************************************
 * 函数: NVIC_Configuration
 * 功能: 配置NVIC中断优先级分组
 * 参数: 无
 * 返回: 无
 * 说明: 设置为NVIC_PriorityGroup_2
 *       2位抢占优先级(Preemption Priority, 0-3) + 2位响应优先级(Sub Priority, 0-3)
 *       抢占优先级高的中断可以打断抢占优先级低的中断（实现中断嵌套）
 *       相同抢占优先级的中断按响应优先级决定执行顺序，但不支持嵌套
 *       该配置适用于多数嵌入式应用场景
 ******************************************************************************/
void NVIC_Configuration(void)
{
    /* 设置NVIC中断分组2：高2位为抢占优先级，低2位为响应优先级 */
    NVIC_PriorityGroupConfig(NVIC_PriorityGroup_2);
}
