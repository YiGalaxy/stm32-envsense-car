/******************************************************************************
 * 文件名: oled.h
 * 描  述: OLED显示驱动模块头文件
 * 说  明: SSD1306 128x64 单色OLED I2C驱动接口声明
 *         采用帧缓冲机制: 先操作内存缓冲区，调用刷新函数统一更新屏幕
 *         提供点阵绘制、ASCII字符串、中文字符、单色位图等显示接口
 *         帧缓冲区大小: 128列 × (64行/8) = 1024字节
 ******************************************************************************/
#ifndef __OLED_H
#define __OLED_H

#include "sys.h"

/**
 * @brief  OLED初始化
 * @note   初始化I2C通信引脚(SCL/SDA)和复位引脚(RES)
 *         向SSD1306控制器发送初始化序列(显示关闭/时钟分频/复用比/偏移/电荷泵等)
 *         配置完成后自动清屏，OLED进入正常工作状态(显示开启)
 *         系统上电后必须在所有其他显示操作之前调用一次
 */
void OLED_Init(void);

/**
 * @brief  清空帧缓冲区
 * @note   将1024字节帧缓冲区所有像素数据置0(全部熄灭)
 *         此操作仅影响内存中的缓冲区，不会立即更新到物理屏幕
 *         需要调用 OLED_Refresh() 才会将清屏效果显示到OLED上
 */
void OLED_Clear(void);

/**
 * @brief  刷新OLED显示
 * @note   将帧缓冲区全部数据(1024字节)通过I2C批量传输到SSD1306的GDDRAM中
 *         每次调用完整重绘整个屏幕，缓冲区的内容决定最终显示画面
 *         建议在主循环中以固定帧率(如50~60Hz)周期性调用
 */
void OLED_Refresh(void);

/**
 * @brief  在指定坐标绘制单个像素点
 * @param  x 像素列坐标(0~127，最左为0)，超出范围自动忽略
 * @param  y 像素行坐标(0~63，最上为0)，超出范围自动忽略
 * @param  t 像素颜色值：1=点亮(白色/蓝色)，0=熄灭(黑色)
 * @note   直接操作帧缓冲区中对应的位，需调用 OLED_Refresh() 后生效
 */
void OLED_DrawPoint(u8 x, u8 y, u8 t);

/**
 * @brief  在OLED指定位置显示ASCII字符串
 * @param  x    起始列坐标(0~127)
 * @param  y    起始行坐标(0~63)
 * @param  str  待显示字符串指针(以'\0'结尾的ASCII字符数组)
 * @param  mode 颜色模式：1=正常显示(前景白背景黑)，0=反色显示(前景黑背景白)
 * @note   使用6×8像素等宽字体
 *         字符串超出单行宽度(21字符)时自动换到下一行(8像素行距)
 */
void OLED_ShowString(u8 x, u8 y, const u8 *str, u8 mode);

/**
 * @brief  在OLED指定区域显示单色位图
 * @param  pic 位图数据指针(数据按"逐列逐页"方式排列，与SSD1306 GDDRAM一致)
 * @param  x   左上角列坐标(0~127)
 * @param  y   左上角行坐标(0~63)
 * @param  w   位图宽度(像素数)，必须是8的倍数
 * @param  h   位图高度(像素数)，必须是8的倍数
 * @note   数据排列: 每列从上到下按页(1页=8行)连续存储
 *         适合显示全屏(128×64)或图标(如16×16/32×32)等规则位图
 */
void OLED_ShowImage(const u8 *pic, u8 x, u8 y, u8 w, u8 h);

/**
 * @brief  获取帧缓冲区指针
 * @return uint8_t* 指向1024字节帧缓冲区的指针
 *         缓冲区布局: 128列 × 8页(每页8行) = 1024字节
 *         索引计算: buffer[page * 128 + col]
 * @note   外部模块可通过此指针直接读写缓冲区数据
 *         修改缓冲区内容后需调用 OLED_Refresh() 才能更新到屏幕
 */
uint8_t *OLED_GetFrameBuffer(void);

/**
 * @brief  在OLED指定位置显示图片(支持任意尺寸的灵活模式)
 * @param  x    左上角列坐标(0~127)
 * @param  y    左上角行坐标(0~63)
 * @param  w    图片宽度(像素数)，无需是8的倍数
 * @param  h    图片高度(像素数)，无需是8的倍数
 * @param  pic  图片数据指针(逐列逐页排列)
 * @param  mode 颜色模式：1=原样显示, 0=反色显示
 * @note   与OLED_ShowImage()不同，此函数支持非8倍数的任意宽高
 *         自动处理跨SSD1306内部页(每页8行)的数据拼接
 *         适合显示不规则尺寸的小图标
 */
void OLED_ShowPicture(u8 x, u8 y, u8 w, u8 h, const u8 *pic, u8 mode);

/**
 * @brief  显示中文字符
 * @param  x     起始列坐标(0~127)
 * @param  y     起始行坐标(0~63)
 * @param  num   汉字在字库数组中的索引编号(0 ~ 字库总字数-1)
 * @param  size1 字体大小(常用可选值: 12/16/24，单位像素)
 * @param  mode  颜色模式：1=正常显示, 0=反色显示
 * @note   必须配合外置汉字字库数据数组一起使用
 *         字库数组的具体数据结构取决于底层实现(逐行或逐列存储模式)
 *         仅能显示字库中预定义过的汉字，不支持动态造字
 */
void OLED_ShowChinese(u8 x, u8 y, u8 num, u8 size1, u8 mode);

#endif
