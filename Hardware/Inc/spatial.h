/******************************************************************************
 * 文件名: spatial.h
 * 描  述: 空间扫描模块头文件
 * 说  明: 定义前方180°扇形区域探测接口
 *         配合舵机+超声波实现多角度环境扫描
 *         用于避障路径决策(最长可通行扇区算法)
 *         9扇区: 30°~150°每15°一个测量点
 ******************************************************************************/
#ifndef __SPATIAL_H
#define __SPATIAL_H

#include "stm32f10x.h"

/*==============================================================================
 * 扇区参数定义
 * 扫描范围: 前方180°(从最左30°到最右150°)
 * 扇区间隔: 15°(兼顾分辨率和扫描速度)
 * 扇区数量: 9个(180°/15° + 1 = 13, 取30°~150°共9个)
 *============================================================================*/
#define SCT_NUM     9           /* 扇区总数 */
#define SCT_ANGLE   15          /* 相邻扇区间的角度间隔(度) */
#define SCT_MIN     30          /* 扫描起始角度(最左侧，单位度) */
#define SCT_MAX     150         /* 扫描结束角度(最右侧，单位度) */
#define SCT_CENTER  4           /* 正前方扇区在数组中的索引(0-based)，对应90° */

/*==============================================================================
 * 扫描结果枚举值
 * Spatial_Scan()的返回值，标识最优通行方向
 *============================================================================*/
#define PATH_FORWARD   0        /* 正前方通畅(最长可通行段覆盖扇区4) */
#define PATH_LEFT      1        /* 左侧更优(最佳通道中心偏左) */
#define PATH_RIGHT     2        /* 右侧更优(最佳通道中心偏右) */
#define PATH_ESCAPE    3        /* 全部扇区均不可通行 → 需执行脱困程序 */

/**
 * @brief  执行一次空间扫描
 * @param  threshold 障碍判定距离阈值(单位cm)
 *                   超声波测距值 > threshold 视为可通行
 *                   典型值: 30~50cm(根据场景调整)
 * @return uint8_t   最优通行方向枚举值:
 *         - PATH_FORWARD: 正前方可通行
 *         - PATH_LEFT:    推荐左转
 *         - PATH_RIGHT:   推荐右转
 *         - PATH_ESCAPE:  无可通行路径
 * @note   阻塞执行，总耗时约1.35s(9扇区 × 150ms/扇区)
 *         扫描过程中舵机会依次转到各个角度
 *         扫描完成后舵机自动回到90°(正前方)
 *         结果可通过Spatial_GetMap()获取详细距离数据
 */
uint8_t Spatial_Scan(uint16_t threshold);

/**
 * @brief  获取最新一次扫描的距离地图
 * @return uint16_t* 指向SCT_NUM(9)个uint16_t类型距离数据的指针
 *         返回值按扇区索引[0~8]排列，分别对应30°~150°方向
 * @note   可直接读取或打印各方向距离用于调试
 *         仅在Spatial_Scan()调用后包含有效数据
 *         有效距离范围: 0~400cm, 0表示无回波(无障碍)
 */
uint16_t *Spatial_GetMap(void);

#endif
