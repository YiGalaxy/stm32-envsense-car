/******************************************************************************
 * 文件名: sr04.h
 * 描  述: 超声波测距模块头文件
 * 说  明: HC-SR04超声波测距模块接口声明。
 *         提供初始化、触发测距和获取距离三个核心函数。
 *         硬件连接: Trig → PA7, Echo → PA6 (TIM3_CH1输入捕获)
 ******************************************************************************/
#ifndef __SR04_H
#define __SR04_H

#include "stm32f10x.h"

/**
 * @brief  初始化超声波模块。
 *         配置Trig引脚(PA7)为推挽输出，Echo引脚(PA6)为浮空输入，
 *         并初始化TIM3通道1为输入捕获模式，用于测量Echo脉宽。
 */
void SR04_Init(void);

/**
 * @brief  触发超声波模块发送测距脉冲。
 *         向Trig引脚(PA7)输出10us高电平脉冲，
 *         复位捕获状态机和完成标志。
 *         @note 通常在调用 SR04_GetDistance() 时内部自动调用，
 *                也可单独用于调试。
 */
void SR04_StartMeasure(void);

/**
 * @brief  执行一次超声波测距并返回距离值。
 *         内部调用 SR04_StartMeasure() 触发测距，
 *         轮询等待捕获中断完成，再换算为厘米值返回。
 * @return float 距离值，单位厘米(cm)；超时或无效返回 -1。
 */
float SR04_GetDistance(void);

#endif
