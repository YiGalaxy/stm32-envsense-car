/******************************************************************************
 * 文件名: avoid.h
 * 描  述: 自动避障模块头文件
 * 说  明: 封装红外防撞 + 超声波空间扫描避障逻辑
 *         主循环中调用 Mode_Avoid() 即可运行三级递进避障状态机
 *         红外优先响应近距碰撞，超声波用于中距路径规划
 ******************************************************************************/
#ifndef __AVOID_H
#define __AVOID_H

#include "stm32f10x.h"

/* 外部变量声明：超声波测距结果(单位cm)
 * distance   - 前向距离(舵机在90°正前方)
 * distance_L - 左侧距离(舵机在0°极限左)
 * distance_R - 右侧距离(舵机在180°极限右)
 * 以上变量定义在 main.c 中
 */
extern float distance, distance_L, distance_R;

/**
 * @brief  红外传感器GPIO初始化
 * @note   配置PF5~PF8为上拉输入模式
 *         PF7 = 左红外, PF8 = 右红外
 *         传感器输出低电平(0)表示检测到障碍物
 *         需在调用Mode_Avoid()前先调用此函数完成初始化
 */
void IR_Init(void);

/**
 * @brief  自动避障模式主状态机
 * @note   三级递进避障策略(需在主循环中周期调用):
 *         红外防撞(近距紧急) → 超声波空间扫描(9扇区) → 试探转向 → 掉头
 *   状态0: 前进(正常行驶，持续测距)
 *   状态1: 空间扫描(后退 → Spatial_Scan → 决策)
 *   状态2: 试探转向(按推荐方向旋转，每方向最多3次，可换向)
 *   状态3: 掉头(连续左转至180°脱困)
 * @warning 仅在 work_mode == MODE_AUTO 时调用
 *          依赖 speed 全局变量(行驶速度)
 *          依赖 avoid_dist 配置(避障触发距离阈值)
 */
void Mode_Avoid(void);

#endif
