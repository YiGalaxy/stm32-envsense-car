/******************************************************************************
 * 文件名:		servo.h
 * 描  述:		舵机控制模块头文件
 * 说  明:		声明舵机初始化及角度设置函数。
 *				使用TIM2_CH1(PA0)输出50Hz标准舵机PWM。
 *				角度范围0~180度，对应脉宽0.5ms~2.5ms。
 ******************************************************************************/
#ifndef __SERVO_H
#define __SERVO_H

#include "stm32f10x.h"

/**
 * @brief  舵机初始化
 * @note   配置TIM2_CH1 PWM输出，50Hz，初始中位90度
 */
void Servo_Init(void);

/**
 * @brief  设置舵机角度
 * @param  angle 目标角度（度），范围 0~180
 * @note   内部进行角度钳位和线性映射
 */
void Servo_SetAngle(float angle);

#endif
